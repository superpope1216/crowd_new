package com.wisedu.crowd.entity.datacode.user;
/**
 *  企业认证类型   
 * @author 11651
 *
 */
public enum EnterpriseQualificateTypeEnum {

	
	THREE("1","三证"),
	ONE("2","三证合一");
	
	private String code;
	private String name;
	
	
	EnterpriseQualificateTypeEnum(String code,String name){
		this.code=code;
		this.name=name;
	}
	public String getCode() {
		return code;
	}
	public void setCode(String code) {
		this.code = code;
	}
	public String getName() {
		return name;
	}
	public void setName(String name) {
		this.name = name;
	}
	
}
