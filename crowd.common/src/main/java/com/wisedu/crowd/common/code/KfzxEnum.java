public enum KfzxEnum {
	/**
	 * 南京
	 */
	NJ("1", "南京"),
	/**
	 * 济南
	 */
	JN("2", "济南"),
	/**
	 * 长沙
	 */
	CS("3","长沙"),
	/**
	 * 其他
	 */
	QT("6","其他");
	private String code;

	private String name;

	KfzxEnum(String code, String name) {
		this.code = code;
		this.name = name;
	}

	public String getCode() {
		return code;
	}

	public void setCode(String code) {
		this.code = code;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}
}
