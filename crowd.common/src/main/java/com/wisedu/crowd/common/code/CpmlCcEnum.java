package com.wisedu.crowd.common.code;

/**
 * 产品目录层次信息
 * @author wisedu
 *
 */
public enum CpmlCcEnum {
	ZERO("0","1"),
	
	ONE("1","1"),
	
	TWO("2","2"),
	THREE("3","3");
    private String code;

    private String name;

    CpmlCcEnum(String code, String name) {
        this.code = code;
        this.name = name;
    }

    public String getCode() {
        return code;
    }

    public void setCode(String code) {
        this.code = code;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
}
