package com.wisedu.crowd.common.code;

public enum LogTypeEnum {

	    QUERY("0","查询"),
	    ADD("0","新增"),
	    MODIFY("0","修改"),
	    DELETE("0","删除");
	    private String    code;

	    private String name;

	    LogTypeEnum(String code, String name) {
	        this.code = code;
	        this.name = name;
	    }

	    public String getCode() {
	        return code;
	    }

	    public void setCode(String code) {
	        this.code = code;
	    }

	    public String getName() {
	        return name;
	    }

	    public void setName(String name) {
	        this.name = name;
	    }
}
