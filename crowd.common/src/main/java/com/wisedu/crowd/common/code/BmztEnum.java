package com.wisedu.crowd.common.code;
/**
 * 报名状态
 * Date:     2018年1月8日 上午10:48:47 <br/>
 * @author   dell
 * @version  
 * @since    JDK 1.6
 * @see 	 
 */
public enum BmztEnum {

    /**
     * 预报名
     */
    YYBM(1, "预约报名"),
    
    /**
     * 已报名
     */
    ZSBM(2, "已报名"),
    
    /**
     * 取消报名
     */
    QXBM(3, "取消报名");
    
    private int code;
    
    private String name;
    
    BmztEnum(int code, String name){
        this.code = code;
        this.name = name;
    }

    public int getCode() {
        return code;
    }

    public void setCode(int code) {
        this.code = code;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }
    
}

